@dataclass
class CompositeCurveType(AbstractCurveType):
    """
    A CompositeCurve is defined by a sequence of (orientable) curves such that
    the each curve in the sequence terminates at the start point of the
    subsequent curve in the list.

    :ivar curve_member: This element references or contains one curve in
        the composite curve. The curves are contiguous, the collection
        of curves is ordered. NOTE: This definition allows for a nested
        structure, i.e. a CompositeCurve may use, for example, another
        CompositeCurve as a curve member.
    """
    curve_member: List["CurveMember"] = field(
        default_factory=list,
        metadata={
            "name": "curveMember",
            "type": "Element",
            "namespace": "http://www.opengis.net/gml",
            "min_occurs": 1,
        }
    )


@dataclass
class CompositeSolidType(AbstractSolidType):
    """A composite solid is a geometry type with all the geometric properties
    of a (primitive) solid.

    Essentially, a composite solid is a collection of solids that join
    in pairs on common boundary surfaces and which, when considered as a
    whole, form a single solid.

    :ivar solid_member: This element references or contains one solid in
        the composite solid. The solids are contiguous. NOTE: This
        definition allows for a nested structure, i.e. a CompositeSolid
        may use, for example, another CompositeSolid as a member.
    """
    solid_member: List["SolidMember"] = field(
        default_factory=list,
        metadata={
            "name": "solidMember",
            "type": "Element",
            "namespace": "http://www.opengis.net/gml",
            "min_occurs": 1,
        }
    )


@dataclass
class CompositeSurfaceType(AbstractSurfaceType):
    """A CompositeSurface is defined by a set of orientable surfaces.

    A composite surface is geometry type with all the geometric
    properties of a (primitive) surface. Essentially, a composite
    surface is a collection of surfaces that join in pairs on common
    boundary curves and which, when considered as a whole, form a single
    surface.

    :ivar surface_member: This element references or contains one
        surface in the composite surface. The surfaces are contiguous.
        NOTE: This definition allows for a nested structure, i.e. a
        CompositeSurface may use, for example, another CompositeSurface
        as a member.
    """
    surface_member: List["SurfaceMember"] = field(
        default_factory=list,
        metadata={
            "name": "surfaceMember",
            "type": "Element",
            "namespace": "http://www.opengis.net/gml",
            "min_occurs": 1,
        }
    )


